function requiredGeneratedContentLists(zones: Zone[], contentPools: RmgContentPool[]): Set<string> {
  const names = new Set<string>();
  for (const zone of zones) {
    for (const object of zone.objects) {
      if (object.includeList?.startsWith("visual_editor_")) {
        names.add(object.includeList);
      }
    }
  }
  for (const pool of contentPools) {
    for (const poolGroup of pool.groups || []) {
      for (const name of poolGroup.includeLists || []) {
        if (name.startsWith("visual_editor_")) {
          names.add(name);
        }
      }
    }
  }
  return names;
}

