function orientationAnchorName(settings: MapSettings, zones: Zone[]): string {
  return zones.some((zone) => zone.id === settings.orientationAnchor)
    ? settings.orientationAnchor
    : (zones.find((zone) => zone.type === "spawn")?.id || zones[0]?.id || "");
}

function connectionName(edgeData: Edge): string {
  return edgeData.id;
}

function getBaseType(zoneType: string, presets: Record<string, Preset>): string {
  const preset = presets[zoneType];
  return preset ? preset.baseType : zoneType;
}

function poolName(type: string, presets: Record<string, Preset>, guarded: boolean, zone?: Zone): string {
  const suffix = guarded ? "guarded" : "unguarded";
  const baseType = getBaseType(type, presets);
  const hasSpawn = baseType === "spawn" || (zone?.mainObjects && zone.mainObjects.some(mo => mo.type === "Spawn"));
  if (hasSpawn) return `visual_pool_${suffix}_start`;
  if (baseType === "low") return `visual_pool_${suffix}_low`;
  if (baseType === "medium") return `visual_pool_${suffix}_medium`;
  if (baseType === "high") return `visual_pool_${suffix}_high`;
  return `visual_pool_${suffix}_low`;
}

/**
 * Builds a biome rule from explicit mode parts. Shared by the content/meta
 * biome export and (for round-trip classification) the importer.
 */
export function auxBiomeRule(mode: 'own' | 'random' | 'spawn' | 'specific', source?: string, id?: string) {
  if (mode === 'own') return { type: 'MatchMainObject' as const, args: ['0'] };
  if (mode === 'spawn' && source) return { type: 'MatchZone' as const, args: [source] };
  if (mode === 'specific' && id && biomeIds.includes(id)) return { type: 'FromList' as const, args: [id] };
  return { type: 'FromList' as const, args: [] };
}

function biomeRule(zone: Zone, presets: Record<string, Preset>) {
  const baseType = getBaseType(zone.type, presets);
  const hasOwnBiome = baseType === "spawn" || (zone.mainObjects && zone.mainObjects.length > 0);
  if (zone.biomeMode === "own" && hasOwnBiome) {
    return { type: "MatchMainObject" as const, args: ["0"] };
  }
  if (zone.biomeMode === "spawn" && zone.biomeSource) {
    return { type: "MatchZone" as const, args: [zone.biomeSource] };
  }
  if (zone.biomeMode === "specific" && biomeIds.includes(zone.biomeId)) {
    return { type: "FromList" as const, args: [zone.biomeId] };
  }
  return { type: "FromList" as const, args: [] };
}

function editorBiomeFromRule(rule: unknown): {
  mode: Zone["biomeMode"];
  source: string;
  id: string;
} {
  if (!rule || typeof rule !== "object") {
    return { mode: "random", source: "", id: "Grass" };
  }

  const biomeRule = rule as { type?: unknown; args?: unknown };
  const args = Array.isArray(biomeRule.args) ? biomeRule.args : [];
  const firstArgument = typeof args[0] === "string" ? args[0] : "";

  if (biomeRule.type === "MatchMainObject") {
    return { mode: "own", source: "", id: "Grass" };
  }
  if (biomeRule.type === "MatchZone" && firstArgument) {
    return { mode: "spawn", source: firstArgument, id: "Grass" };
  }
  if (biomeRule.type === "FromList" && firstArgument) {
    return { mode: "specific", source: "", id: firstArgument };
  }
  return { mode: "random", source: "", id: "Grass" };
}

function isImportedBiomeUnchanged(zone: Zone): boolean {
  if (!zone.originalZoneBiome) return false;
  const original = editorBiomeFromRule(zone.originalZoneBiome);
  return (
    zone.biomeMode === original.mode &&
    zone.biomeSource === original.source &&
    zone.biomeId === original.id
  );
}


function boundsMatchRule(
  bounds: { min: number; max: number } | null,
  rule: RmgPlacementRule | null
): boolean {
  const rb = rule ? ruleBounds(rule) : null;
  // "any" matches the absence of that rule type, or a rule we can't express as
  // a min/max distance (it then rides through verbatim, e.g. `target`-only).
  if (!bounds) return !rb;
  if (!rb) return false;
  return rb.min === bounds.min && rb.max === bounds.max;
}

function buildObjectPlacementRules(zone: Zone, entry: ZoneObject): RmgPlacementRule[] {
  const roadBounds = resolveDistance(entry.roadDistance);
  const townBounds = resolveDistance(entry.townDistance);

  // Keep the original rules verbatim while the model's distances still match
  // them (the user hasn't touched the Road/Town distance) — this preserves
  // exact values, weights, args and rule types the distance model can't hold.
  if (Array.isArray(entry.rawRules)) {
    let roadRule: RmgPlacementRule | null = null;
    let townRule: RmgPlacementRule | null = null;
    for (const rule of entry.rawRules) {
      if (rule.type === "Road") roadRule = rule;
      else if (rule.type === "MainObject") townRule = rule;
    }
    if (boundsMatchRule(roadBounds, roadRule) && boundsMatchRule(townBounds, townRule)) {
      return entry.rawRules.map((rule) => ({ ...rule }));
    }
  }

  const rules: RmgPlacementRule[] = [];
  const hasTown = (zone.mainObjects || []).length > 0;
  if (roadBounds) {
    rules.push({ type: "Road", args: [], targetMin: roadBounds.min, targetMax: roadBounds.max, weight: 1 });
  }
  if (hasTown && townBounds) {
    rules.push({ type: "MainObject", args: ["0"], targetMin: townBounds.min, targetMax: townBounds.max, weight: 1 });
  }
  return rules;
}

function toMandatoryObject(
  zone: Zone,
  entry: ZoneObject,
  index: number,
  useEntryName = false
): RmgMandatoryObject {
  const base: RmgMandatoryObject = {
    name: useEntryName && entry.name
      ? entry.name
      : `name_${safeName(zone.id).toLowerCase()}_${index + 1}_${safeName(entry.id).toLowerCase()}`
  };
  if (entry.kind === "list" && (entry.rawIncludeLists?.length || entry.includeList)) {
    // Preserve the full set of content lists when the object referenced several.
    base.includeLists = entry.rawIncludeLists?.length ? [...entry.rawIncludeLists] : [entry.includeList!];
  } else if (entry.sid) {
    base.sid = entry.sid;
  }
  if (entry.variant !== null && entry.variant !== undefined && Number.isFinite(Number(entry.variant))) {
    base.variant = Math.trunc(Number(entry.variant));
  }
  if (entry.isMine) base.isMine = true;
  if (entry.owner) base.owner = `Player${entry.owner}`;
  if (entry.designatedEncounter !== undefined) base.designatedEncounter = entry.designatedEncounter;
  if (entry.nestedContent?.length) base.content = entry.nestedContent.map((c) => ({ sid: c.sid, weight: c.weight }));
  // Tri-state: omit isGuarded entirely when unset, so the engine applies its
  // own default instead of being pinned to false.
  if (entry.guarded !== undefined) base.isGuarded = entry.guarded;
  base.soloEncounter = Boolean(entry.soloEncounter);
  const rules = buildObjectPlacementRules(zone, entry);
  if (rules.length) base.rules = rules;
  return base;
}

function buildMandatoryObjects(zone: Zone): RmgMandatoryObject[] {
  const content: RmgMandatoryObject[] = [];
  for (const entry of zone.objects) {
    const count = Math.max(1, Math.min(99, Math.trunc(Number(entry.count) || 1)));
    for (let copy = 0; copy < count; copy += 1) {
      // A user-set name identifies one specific entry, so only the first copy
      // carries it; the rest fall back to auto-generated names.
      content.push(toMandatoryObject(zone, entry, content.length, copy === 0));
    }
  }
  return content;
}

